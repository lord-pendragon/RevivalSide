#!/usr/bin/env node
"use strict";

const fs = require("fs");
const path = require("path");

const ROOT = path.resolve(__dirname, "..");
const SHOP_TABLE_RELATIVE = path.join("StreamingAssets", "ab_script", "luac", "LUA_SHOP_TEMPLET_01.json");
const OUTPUT_RELATIVE = path.join("ExtraAsset", "AB_SCRIPT", "REVIVALSIDE_SHOP_TEMPLET_01_SUPPLEMENT.lua");
const PRODUCT_IDS = [
  2351, 2363, 2352, 2364, 2353, 2354, 2355, 2356, 2357,
  2365, 2358, 2366, 2359, 2360, 2361, 2362, 2367, 2368,
];

function main() {
  const options = parseArgs(process.argv.slice(2));
  const sourcePath = resolveSourcePath(options.source);
  const records = loadRestoredRows(sourcePath);
  const lua = renderSupplementLua(records, sourcePath);
  const outputs = new Set();

  outputs.add(options.output || path.join(ROOT, "client-overrides", OUTPUT_RELATIVE));
  for (const gameRoot of options.gameRoots) outputs.add(path.join(gameRoot, OUTPUT_RELATIVE));
  for (const managedDir of options.managedDirs) outputs.add(path.join(resolveGameRootFromManagedDir(managedDir), OUTPUT_RELATIVE));
  for (const runtimeRoot of options.runtimeRoots) outputs.add(path.join(runtimeRoot, "client-overrides", OUTPUT_RELATIVE));

  for (const outputPath of outputs) {
    writeIfChanged(outputPath, lua);
    console.log(`[admin-coin-shop-assets] wrote ${path.relative(ROOT, outputPath) || outputPath}`);
  }

  console.log(`[admin-coin-shop-assets] restored ${records.length} Admin Coin cash products`);
}

function parseArgs(args) {
  const options = {
    source: "",
    output: "",
    gameRoots: [],
    managedDirs: [],
    runtimeRoots: [],
  };

  for (let index = 0; index < args.length; index += 1) {
    const arg = args[index];
    if (arg === "--source") options.source = requireValue(args, ++index, arg);
    else if (arg === "--output") options.output = path.resolve(requireValue(args, ++index, arg));
    else if (arg === "--game-root") options.gameRoots.push(path.resolve(requireValue(args, ++index, arg)));
    else if (arg === "--managed-dir") options.managedDirs.push(path.resolve(requireValue(args, ++index, arg)));
    else if (arg === "--runtime-root") options.runtimeRoots.push(path.resolve(requireValue(args, ++index, arg)));
    else if (arg === "--help" || arg === "-h") {
      printUsage();
      process.exit(0);
    } else {
      throw new Error(`Unknown argument: ${arg}`);
    }
  }

  return options;
}

function requireValue(args, index, name) {
  const value = args[index];
  if (!value) throw new Error(`${name} requires a value`);
  return value;
}

function printUsage() {
  console.log([
    "Usage: node tools/restore-admin-coin-shop-assets.js [options]",
    "",
    "Options:",
    "  --source <file>       Source LUA_SHOP_TEMPLET_01.json with restored rows",
    "  --output <file>       Write a specific supplement Lua file",
    "  --game-root <dir>     Also write CounterSide/ExtraAsset override",
    "  --managed-dir <dir>   Derive CounterSide root from Data/Managed",
    "  --runtime-root <dir>  Also write runtime client-overrides copy",
  ].join("\n"));
}

function resolveSourcePath(explicitPath) {
  const candidates = explicitPath
    ? [path.resolve(explicitPath)]
    : [
        path.join(ROOT, "gameplay-jsons", SHOP_TABLE_RELATIVE),
        path.join(ROOT, "gameplay-tables-json", SHOP_TABLE_RELATIVE),
      ];
  const found = candidates.find((candidate) => fs.existsSync(candidate));
  if (!found) throw new Error(`Could not find ${SHOP_TABLE_RELATIVE}`);
  return found;
}

function loadRestoredRows(sourcePath) {
  const parsed = JSON.parse(fs.readFileSync(sourcePath, "utf8"));
  const records = Array.isArray(parsed.records) ? parsed.records : [];
  return PRODUCT_IDS.map((productId) => {
    const record = records.find((item) => Number(item && item.m_ProductID) === productId);
    if (!record) throw new Error(`Product ${productId} is missing from ${sourcePath}`);
    return normalizeRestoredRow(record);
  });
}

function normalizeRestoredRow(record) {
  const restored = JSON.parse(JSON.stringify(record));
  delete restored.listContentsTagAllow;
  delete restored.listContentsTagIgnore;
  return restored;
}

function renderSupplementLua(records, sourcePath) {
  const lines = [
    "-- Generated by tools/restore-admin-coin-shop-assets.js",
    `-- Source: ${sourcePath.replace(/\\/g, "/")}`,
    "if type(m_ShopTable) ~= \"table\" then",
    "  m_ShopTable = {}",
    "end",
    "",
    "local function RevivalSideFindShopProductIndex(productId)",
    "  for index = 1, #m_ShopTable do",
    "    local row = m_ShopTable[index]",
    "    if row ~= nil and row.m_ProductID == productId then",
    "      return index",
    "    end",
    "  end",
    "  return nil",
    "end",
    "",
    "local function RevivalSideUpsertShopProduct(row)",
    "  if row == nil then",
    "    return",
    "  end",
    "  local index = RevivalSideFindShopProductIndex(row.m_ProductID)",
    "  if index ~= nil then",
    "    m_ShopTable[index] = row",
    "  else",
    "    m_ShopTable[#m_ShopTable + 1] = row",
    "  end",
    "end",
    "",
  ];

  for (const record of records) {
    lines.push(`RevivalSideUpsertShopProduct(${renderLuaValue(record, 0)})`);
  }

  return `${lines.join("\n")}\n`;
}

function renderLuaValue(value, indent) {
  if (value === null || typeof value === "undefined") return "nil";
  if (typeof value === "number") return Number.isFinite(value) ? String(value) : "0";
  if (typeof value === "boolean") return value ? "true" : "false";
  if (typeof value === "string") return quoteLuaString(value);
  if (Array.isArray(value)) {
    if (value.length === 0) return "{}";
    const nextIndent = indent + 2;
    const inner = value.map((item) => `${" ".repeat(nextIndent)}${renderLuaValue(item, nextIndent)}`).join(",\n");
    return `{\n${inner}\n${" ".repeat(indent)}}`;
  }
  if (typeof value === "object") {
    const keys = Object.keys(value).filter((key) => typeof value[key] !== "undefined" && value[key] !== null);
    if (keys.length === 0) return "{}";
    const nextIndent = indent + 2;
    const inner = keys
      .map((key) => `${" ".repeat(nextIndent)}[${quoteLuaString(key)}] = ${renderLuaValue(value[key], nextIndent)}`)
      .join(",\n");
    return `{\n${inner}\n${" ".repeat(indent)}}`;
  }
  return quoteLuaString(String(value));
}

function quoteLuaString(value) {
  return `"${value
    .replace(/\\/g, "\\\\")
    .replace(/\r/g, "\\r")
    .replace(/\n/g, "\\n")
    .replace(/\t/g, "\\t")
    .replace(/"/g, "\\\"")}"`;
}

function resolveGameRootFromManagedDir(value) {
  let managedDir = path.resolve(value);
  if (path.basename(managedDir).toLowerCase() === "assembly-csharp.dll") managedDir = path.dirname(managedDir);
  if (path.basename(managedDir).toLowerCase() !== "managed") {
    const nested = path.join(managedDir, "Data", "Managed");
    if (fs.existsSync(path.join(nested, "Assembly-CSharp.dll"))) managedDir = nested;
  }
  if (!fs.existsSync(path.join(managedDir, "Assembly-CSharp.dll"))) {
    throw new Error(`Assembly-CSharp.dll was not found in ${managedDir}`);
  }
  const dataDir = path.dirname(managedDir);
  if (path.basename(dataDir).toLowerCase() !== "data") throw new Error(`Managed dir is not under CounterSide/Data: ${managedDir}`);
  return path.dirname(dataDir);
}

function writeIfChanged(outputPath, content) {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  if (fs.existsSync(outputPath) && fs.readFileSync(outputPath, "utf8") === content) return;
  fs.writeFileSync(outputPath, content, "utf8");
}

main();
